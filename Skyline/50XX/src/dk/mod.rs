#[acmd_script(agent = "donkey", scripts = ["game_specials"], category = ACMD_GAME)]
unsafe fn dk_sideb_grnd(fighter: &mut smash::lua2cpp::L2CAgentBase) {
    let lua_state = fighter.lua_state_agent;
    let module_accessor = smash::app::sv_system::battle_object_module_accessor(lua_state);
    if CANBARREL[get_player_number(module_accessor)] && CANPROJECTILE[get_player_number(module_accessor)] {
        LASTBARREL[get_player_number(module_accessor)] = GLOBALFRAMECOUNT;
        CANBARREL[get_player_number(module_accessor)] = false;
        ItemModule::have_item(module_accessor, smash::cpp::root::app::ItemKind(*ITEM_KIND_BARREL), 0, 0, false, false);
        StatusModule::change_status_request_from_script(module_accessor, *FIGHTER_STATUS_KIND_ITEM_HEAVY_PICKUP, false);
        ItemModule::pickup_item(module_accessor, smash::cpp::root::app::ItemSize{_address: *ITEM_SIZE_HEAVY as u8}, -1, 0, smash::cpp::root::app::QuickItemTreatType{_address: 2 as u8}, smash::cpp::root::app::ItemPickupSearchMode{_address: 0 as u8});
    }
}

